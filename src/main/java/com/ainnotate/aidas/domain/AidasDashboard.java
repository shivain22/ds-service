package com.ainnotate.aidas.domain;

public class AidasDashboard {

    private Integer organisationCount;
    private Integer customerCount;
    private Integer vendorCount;
    private Integer projectCount;
    private Integer objectCount;
    private Integer userCount;
    private Integer uploadCount;
    private Integer approvedUploadCount;
    private Integer rejectedUploadCount;

    public Integer getOrganisationCount() {
        return organisationCount;
    }

    public void setOrganisationCount(Integer organisationCount) {
        this.organisationCount = organisationCount;
    }

    public Integer getCustomerCount() {
        return customerCount;
    }

    public void setCustomerCount(Integer customerCount) {
        this.customerCount = customerCount;
    }

    public Integer getVendorCount() {
        return vendorCount;
    }

    public void setVendorCount(Integer vendorCount) {
        this.vendorCount = vendorCount;
    }

    public Integer getProjectCount() {
        return projectCount;
    }

    public void setProjectCount(Integer projectCount) {
        this.projectCount = projectCount;
    }

    public Integer getObjectCount() {
        return objectCount;
    }

    public void setObjectCount(Integer objectCount) {
        this.objectCount = objectCount;
    }

    public Integer getUserCount() {
        return userCount;
    }

    public void setUserCount(Integer userCount) {
        this.userCount = userCount;
    }

    public Integer getUploadCount() {
        return uploadCount;
    }

    public void setUploadCount(Integer uploadCount) {
        this.uploadCount = uploadCount;
    }

    public Integer getApprovedUploadCount() {
        return approvedUploadCount;
    }

    public void setApprovedUploadCount(Integer approvedUploadCount) {
        this.approvedUploadCount = approvedUploadCount;
    }

    public Integer getRejectedUploadCount() {
        return rejectedUploadCount;
    }

    public void setRejectedUploadCount(Integer rejectedUploadCount) {
        this.rejectedUploadCount = rejectedUploadCount;
    }
}
